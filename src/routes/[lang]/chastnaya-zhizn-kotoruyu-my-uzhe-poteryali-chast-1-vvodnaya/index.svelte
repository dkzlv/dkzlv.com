<h2 id="часть-1-классификация-заболеваний">
  Часть 1: Классификация заболеваний
</h2>
<p>
  В каком-то смысле право на частную жизнь консолидирует все остальные права: на
  голосование, свободу самовыражения, равные возможности и так далее. Достаточно
  потерять право на частую жизнь и всё, тобi пiзда. Большинство людей влёгкую
  попадают в ловушку мышления
  <em>"Мне нечего скрывать, какое мне дело до приватности?"</em>
  . Распространённая ошибка. Беспокоиться надо не о том, что ты хочешь скрыть, а
  о том, что ты даже не думаешь скрывать.
</p>
<p>
  Тема настолько обширная, что пришлось придумать свою категоризацию. Я долго
  над этим думал (минут 10, не меньше) и пришёл к такой мысли: самое важное
  —&nbsp;это очевидность того, кто хранит данные пользователя, что это за данные
  и как ими манипулируют. Выходит три переменных: актор, данные, манипуляция.
  Всего-то трёхмерная матрица —&nbsp;помнишь, я писал, что даже тупой поймёт
  суть статьи? Не помнишь? Я тоже. Видимо, не писал.
</p>
