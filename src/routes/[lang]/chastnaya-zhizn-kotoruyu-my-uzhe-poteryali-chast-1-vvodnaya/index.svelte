<h4 id="скрипты">Скрипты</h4>
<p>
  Скрипты. Скрипты везде. Скрипты в твоём телефоне, в холодильнике и телевизоре,
  в вибраторе и духовке, в автоматической оконной роллете и ротационном
  рекламном баннере, в поезде метро и опять в вибраторе. Они везде. В моём блоге
  тоже.
</p>
<p>Немного истории веба.</p>
<p>
  До повальной адаптации поисковиков существовали каталоги. Ну вот открыл ты
  браузер в 1999 году, куда идти, что делать? Гугла нет. Правильно, надо идти в
  каталоги. Это были модерируемые людьми наборы сайтов, на которых ты мог
  узнать, где стоит узнать, чо там у хохлов, а где можно поиграть в онлайн-нарды
  с онлайн-армянами. Чтобы как-то сортировать сайты по популярности, создатели
  каталогов придумали предлагать разработчикам сайтов встраивать свои счётчики
  —&nbsp;выглядят они как убогие неуместные картинки с писькомером от создателя
  сайта, показывающие количество посетителей посетителей и уникальных
  посетителей. Уже тогда разработчики продали вас с потрохами, потому что эти
  счётчики по сути открывали картинку с сайта каталога. Это — запрос. Запрос
  отсылает кучу данных на сервер. Сервер вас узнаёт. Вы в жопе, все знают, что у
  вас есть фетиш на нарды с армянами. Теперь каталог знает, сколько уникальных
  посетителей у сайта, и по этим данным сортирует каталог. Вуаля!
</p>
<p>
  Потом пришли поисковики, и всё стало внезапно сложнее. Поисковик сам посещает
  сайты, скачивает все страницы и осуществляет по ним поиск. Главный вопрос
  такой: как ему понять, что из 20М страниц по запросу "как совиршить суецыд" он
  посоветовал самый лучший результат?
  <del>
    <em>Ответ: в идеале пользователь должен пропасть с радаров</em>
  </del>
  . В отличие от каталога, он не знает, как себя ведёт пользователь на странице,
  не знает, как часто тот туда приходит. Ему не на что опираться.
</p>
<h3 id="кто-этот-теневой-получатель">Кто этот Теневой получатель?</h3>
<p>
  Поисковики не знали, как улучшить результаты поиска. Тогда Google и пришёл к
  абсолютно гениальному решению.
</p>
<p>
  Google нужны данные с сайтов. Нужно каким-то образом на этот сайт поставить
  свой скрипт. Как? Дать что-то сверхинтересное для разработчика. В 2003 году
  они запустили AdSense — свою площадку для размещаемой контекстной рекламы. Эта
  площадка позволяла разработчику сайта зарабатывать с просмотров баннеров на
  своём сайте, достаточно было только разместить один малюсенький скрипт.
  Маленький шаг для компании, огромный пиздос для человечества.
</p>
<p>
  Вторым шагом был запуск Google Analytics. Помимо денег все разработчики хотели
  всё больше и больше знать о том, как пользователи ведут себя на сайтах: какие
  страницы посещают, сколько там проводят времени, как быстро и в каких
  направлениях отваливаются. Google запустил бесплатный сервис веб-аналитики для
  всех: "просто встрой скрипт, пожалуйста, не тяни, делай, я хочу обмазаться
  твоими данными" —&nbsp;томно шептал на ухо Google всем разработчикам вокруг.
</p>
<p>
  Согласно
  <a
    href="https://trends.builtwith.com/analytics/Google-Analytics"
    target="_blank">
    BuiltWith
  </a>
  , из миллиона самых популярных сайтов в мире 68% имеют Google Analytics. Из
  10k самых популярных — 88%. У
  <a href="https://trends.builtwith.com/ads/Google-Adsense" target="_blank">
    AdSense
  </a>
  пенетрация поменьше —&nbsp;16% и 33% соответственно. Ты под колпаком, чудило.
  Знаю, твои ладошки вспотели, стало страшно и немного спёрло дыхание. Я хочу,
  чтобы ты, дорогой читатель, обосрался, поэтому сообщу тебе то, чего ты боишься
  больше всего:
  <a href="https://arxiv.org/abs/1907.06520" target="_blank">93% порносайтов</a>
  имеют встроенные скрипты. Google знает, что ты любишь то, что в твоей стране
  осуждается.
</p>
<p>
  Нет-нет, не поймите меня неверно, Google не единственные, кто этим промышляет.
  <a
    href="https://trends.builtwith.com/analytics/Facebook-Pixel"
    target="_blank">
    40% топ 10k сайтов имеют трекинговый скрипт Facebook
  </a>
  . Automattic (компания, о которой ты вообще никогда не слышал) разработала
  движок Wordpress, они же предлагают у себя этот сайт разместить; суммарная
  посещаемость —&nbsp;
  <a href="https://ru.wordpress.com/activity/" target="_blank">
    полмиллиарда человек в месяц
  </a>
  . Видите крутую плавающую кнопку с лайками поста? Это может быть скрипт ребят
  из AddThis, у которых
  <a href="https://trends.builtwith.com/widgets/AddThis" target="_blank">
    26% из топ 10k сайтов
  </a>
  . Видите красивые стильные иконки на сайте? Есть вероятность, что на сайте
  стоит скрипт
  <a href="https://fontawesome.com/" target="_blank">FontAwesome</a>
  ,
  <a href="https://trends.builtwith.com/widgets/Font-Awesome" target="_blank">
    встроенный на 46% тех же сайтов
  </a>
  .
</p>
<p>
  Вы всего этого не знаете, а оно так. Если встраивается скрипт, скрипт может
  делать всё, что может делать JavaScript. Главным образом, конечно, посылать
  запросы, рассказывая своему создателю обо всём, что видит.
</p>
<h4 id="решение">Решение</h4>
<p>
  Ты пока не знаешь, почему весь этот сбор данных плох, но, просто поверь, что
  ты бы хотел его остановить. Он не в твоих интересах.
</p>
<p>
  Я вижу несколько вариантов. Самый простой и пока работающий сейчас
  —&nbsp;пользоваться
  <a href="https://brave.com/" target="_blank">браузером Brave</a>
  . Он по сути идентичен браузеру Chrome, только в нём нет рук Google, это
  частная компания, во главе, кстати, с одним из создателей языка JavaScript
  Бренданом Эйхом. Плюсов несколько:
</p>
<ul>
  <li>
    в браузер встроен хороший блокировщик всего этого описанного дерьма. Это не
    гарантирует тебе защиту от трекеров, и некоторые данные всё равно будут
    утекать, но кратно, кратно меньше;
  </li>
  <li>
    у браузера открытый исходный код, то есть ты можешь пойти и
    <a href="https://github.com/brave" target="_blank">почитать код</a>
    , который заставляет браузер работать. В идеале это значит, что в него не
    получится встроить говна, не обратив на это внимание сообщества.
  </li>
</ul>
<p>
  Молю, не пользуйся расширениями в браузере. Это полная дыра, об этом еще будет
  пост.
</p>
<h3 id="как-же-приложения">Как же приложения?</h3>
<p>
  Я осознанно опустил в статье всё, связанное с обычными приложениями на
  смартфоны и программами на компьютеры. Они пришли несколько позже, когда
  махина сбора данных уже работала в полную силу, поэтому эти новые платформы
  строились изначально с нагибанием приватности в уме. Там всё кратно хуже:
</p>
<ol>
  <li>
    без специального софта нет способа запретить приложению доступ в сеть
    целиком или избирательно;
  </li>
  <li>
    без специального софта нет способа посмотреть, состав запросов, которые
    приложение посылает; если установить этот софт (Charles, например),
    приложение может это узнать (читайте про SSL pinning), а значит тесты по
    определению чистыми не будут;
  </li>
  <li>
    приложения на iOS, Linux, Mac и Windows компилируются в нечитаемый байткод,
    а приложения на Android видоизменяются до почти что нечитаемого состояния.
    Узнать, что происходит внутри приложения практически невозможно или безумно
    сложно;
  </li>
  <li>
    всем приложениям без разбора доступна высокоточная идентификация клиента по
    рекламному ID. Идентификация, в отличие от сайтов, работает между
    приложениями;
  </li>
  <li>
    обе самых популярных платформы принадлежат техногигантам, которые, как мы
    выяснили выше, не очень заинтересованы в сохранении приватности;
  </li>
  <li>
    приложения имеют возможность в тихом режиме запускаться в фоне без вашего
    ведома и делать хрен знает что вообще.
  </li>
</ol>
<p>
  Всех этих проблем нет у сайтов. Приложения еще более закрыты и еще больше
  подтвержены этим загнивающим практикам мониторинга пользователей.
</p>
<pre>
  <code>
    поискать данные про builtwith, но с SDK: сколько приложений имеет firebase,
    сколько приложений имеет crashlytics/microsoft сборщик, сколько onesignal,
    facebook sdk и так далее.
  </code>
</pre>
<p>
  Вся остальная статья же актуальна и для приложений, но механизмы там чуть-чуть
  отличаются, зависят от платформы и их имплементация скрыта, поэтому так
  подробно у меня расписать не выйдет, я не так хорош. Проблемы с сбором данных
  имеется и в приложениях, только по этой проблеме еще меньше информации и
  исследований. Словно бы у кого-то (КХЕ-КХЕ, ГУГЛ, КХА-КХА, ЭППЛ) есть
  бизнес-интерес вставлять эти палки в колёса.
