  <del>CEnvi</del>
  <del>Mocha</del>
  <del>LiveScript</del>
  <del>JScript</del>
  JavaScript. Он даёт тот самый динамизм. Что это такое? Например, вот, нажми на
  эту кнопку:
</p>

