</p>
<p>
  Как вы уже могли догадаться, магии в комплюктерах нет. Если вскрыть этот
  гнойный нарыв, за каждым неудобным и тупым решением мы всегда найдём команду,
  которая по итогу в шоке от того, как её технологию используют. Если почитаете
  спецификации SMTP/POP3 (та штука, которая даёт вам чудо электронной почты), вы
  поразитесь количеству малопонятных костылей.
</p>
<p>
  За любым общением двух машин стоят запросы. Отправка ваших нудесов незнакомцам
  в интернете — это тоже общение, даже если это за вас автоматически без вашего
  ведома делает программа. И с итоговой страницей, которая показывается в
  браузере, самая большая проблема в том, что она умеет эти запросы тоже
  отправлять.
</p>
<h4 id="анатомия-запроса">Анатомия запроса</h4>
<pre>
  <code>Нагуглить байку про создание cookies</code>
</pre>
<p>
  В бородатые годы ребята поняли, что нужно придумать костыль, который позволит
  клиенту между запросами сохранять состояние корзины покупок в
  интернет-магазине. Как обычно, дорога в ад всегда вымощена благими
  намерениями. Тогда и были придуманы те самые cookies, о которых в последние
  годы нас постоянно предупреждают сайты.
</p>
<p>Кукис —&nbsp;это большущая строка такого вида:</p>
<pre>
  <code>PRIVET=1823746587; POKA=ABCDEFG123_123; BLAHBLAH=1234ASLDKJNKJ;</code>
</pre>
<p>
  Было решено, что сервер может просить браузер сохранять эту строчку текста в
  отношении к конкретному хосту, и когда браузер посылает все дальнейшие
  запросы, он обязуется прикладывать к запросу сохранённые кукисы. Процедура
  простая:
</p>
<ol>
  <li>ты вводишь логин и пароль на сайте заказа проституток</li>
  <li>
    сервер проверяет, что всё ок, придумывает несуразную строчку текста (вроде
    <code>1bd5f794-9a91-4fc5-8858-d51a77486573</code>
    ), и запоминает, что тот, кто знает эту секретную строчку текста, тот ты
  </li>
  <li>эту строчку сервер просит браузер запомнить в виде кукис</li>
  <li>все дальнейшие запросы на этот сайт браузер шлёт с этой строчкой</li>
</ol>
<p>
  Вот и всё, мы имеем однозначную и безошибочную идентификацию, и состояние
  корзины не сбрасывается между запросами. Проблема решена, да, но шрапнелью
  зацепило и нашу приватность.
</p>
<p>
  Так в какой момент Google узнаёт о твоих предпочтениях в порно? Сейчас
  разберёмся —&nbsp;мы переходим к стадии галопирующего роста интернет-рекламы.
</p>
<h4 id="javascript-и-динамизм">JavaScript и динамизм</h4>
