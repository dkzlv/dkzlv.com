<p>
  При защищённом соединении все посредники всё равно видят, на какой сервер вы
  обращаетесь (ip-адрес), но этой информации мало для сколь угодно нормального
  анализа, поскольку весь информационный поток зашифрован для каждого
  пользователя индивидуально. Наверняка есть какие-то сложные и неточные
  эвристики для определения хотя бы типа контента — например, если ты уже 10
  минут что-то качаешь с хоста pornhub.com, скорее всего, провайдер знает, что
  это схемы для вязания крючком — но о них беспокоиться не стоит, они неточные.
</p>
<p>
  <strong>Решение</strong>
  : помните про
  <del>незащищённый секс</del>
  незащищённое соединение, там всё идет чистым текстом: данные карт, контент,
  адреса, поля формы, фотографии… Увидите плашку —&nbsp;просто закройте сайт. В
  2019 (AND RISING) году просто стыдно не уметь в HTTPS, считайте, что вас по
  умолчанию такие сайты хотят обуть —&nbsp;глядишь, мир станет чуть безопаснее.
</p>
<h3 id="итоговая-страница-→-">Итоговая страница → ???</h3>
