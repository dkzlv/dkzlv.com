<p>
  Вот тогда, давным давно, еще в прошлом тысячелетии, возможности правильно и
  неожиданно оскорбить прохожего в интернете не было, поэтому её решили
  добавить. JavaScript —&nbsp;это полноценный язык программирования, и на каждой
  странице любому разработчику много чего доступно. Без вашего разрешения он
