<h3 id="девайс-→-dns">Девайс → DNS</h3>
<p>
  Машины привыкли между собой адресовать запросы по т.н. ip-адресам —&nbsp;той
  непонятной херне, которую тебе настраивают безрукие спецы от твоего
  интернет-провайдера, а потом записывают на бумажке со словами "ток не
  потеряйте" (бумажку вы выбрасываете сразу же, конечно). Выглядят они как
  четыре числа, разделённые точками, например 127.0.0.1.
</p>
<p>
  <em>
    Еще хоть один чёрт придёт ко мне в личку с рассказом про ipv6, я за себя не
    ручаюсь.
  </em>
</p>
<p>
  Вот когда вы открываете мой уютный бложек, вы ж вводите не числа, а домен.
  Буквенный. Понятный. Всё это удобство благодаря штуке под названием DNS
  (Domain Name Server, Домен Имя Сервер, понятно всё?). Он переводит понятный
  людям адрес (dkzlv.ru) в понятный машине (127.0.0.1 — РКН, баньте, я не
  против).
</p>
<p>
  Вот мы и узнали первую течь: DNS. Почти все безрукие спецы провайдеров
  настраивают вам DNS-сервера вашего же провайдера. "Для скорости" — говорят
  они. "Где-то тут зарыта выгода" — читаем мы.
</p>
<p>
  Использование DNS-серверов вашего провайдера означает, что вы будете ходить к
  ним и узнавать, какой ip-адрес у какого домена. Кроме того, что это открывает
  прекрасный мир блокировок неугодных сайтов (об этом в другой раз, мои
  либерально-ватниковые друзья), это еще сообщает провайдеру о том, что вы точно
  посещали какой-то сайт.
</p>
<p>
  <strong>Решение</strong>
  : использовать независимые зарубежные DNS-сервера.
  <a href="/setup-dns" target="_blank">Гайд по настройке тут</a>
  , это в буквальном смысле ввести набор чиселок в поле ввода в настройках.
</p>
<h3 id="девайс-→-провайдер-→-дц-→-сервер">Девайс → провайдер → ДЦ → сервер</h3>
<p>
  Есть два типа соединений: защищённое и незащищённое. О незащищённых вам
  сообщит браузер, если он у вас не древний как говно мамонта. Если он не
  древний, как
  <a href="https://ru.wikipedia.org/wiki/Чёрный_носорог" target="_blank">
    говно чёрного носорога
  </a>
  , возможно браузер даже вас настоятельно попросит одуматься и не пользоваться
  таким сайтом.
</p>
<pre>
  <code>
    Вставить пикчу про незащищённое соединение: например, что-то про вскрытые
    конверты и правку сообщений от агентов КГБ
  </code>
</pre>
<p>
  Если видите сообщение про незащищённое соединение, лучше вообще закрыть
  страницу и ничего с ней не делать. Поверьте, с точки зрения информационной
  безопасности это хуже, чем заниматься незащищённым сексом с партнёром, у
  которого от гениталий расходятся радиальные красные полосы.
</p>
<p>
  При незащищённом соединении нет никаких уязвимостей, кроме одной небольшой:
  любой посредник имеет полный контроль над всем, что вы принимаете или
  посылаете, то есть это полное отсутствие безопасности. Мобильные операторы,
  например,
  <a
    href="https://tjournal.ru/tech/62611-pochemu-megafon-bessovestno-pokazyvaet-reklamu-na-chuzhih-saytah"
    target="_blank">
    вставляют на незащищённые сайты
  </a>
  свою полноэкранную рекламу без ведома владельца сайта.
</p>
<p>
  Так что поговорим про защищённое соединение. Чтобы ты мог блеснуть перед
  бабушками у подъезда знаниями, дам краткий ликбез в криптографию (нет, с
  криптовалютами не связано) и работу сети.
</p>
<p>
  Если ты не PhD по криптографии и не запускал свои ICO, достаточно знать, что
  существует два типа шифрования: симметричное и ассиметричное. По простому, в
  симметричном для шифрования и расшифровки используется одинаковый ключ. В
  ассиметричном одним ключом ты зашифровываешь —&nbsp;его часто называют
  <strong>приватным</strong>
  или
  <strong>закрытым</strong>
  — а вторым ключом&nbsp;расшифровываешь —&nbsp;его называют
  <strong>публичным</strong>
  или
  <strong>открытым</strong>
  . Никто не знает, какая там математика замешана (как никто не знает, почему
  такие на вид тяжёлые самолёты летят по небу), но некоторые алгоритмы
  шифрования взломать почти невозможно.
</p>
<p>
  Вот для защищённого соединения используются оба типа шифрования. Чтоб ты не
  заснул, я оформил эту процедуру в виде пьесы.
</p>
